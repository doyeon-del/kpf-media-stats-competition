# -*- coding: utf-8 -*-
"""공용 필터 — 의견 장르(사설·칼럼 등) 판별.

3분류 rubric은 '보도기사의 역할'을 재는 것이라, 사설·칼럼 등 의견 기사는
감시·해결책 쪽으로 구조적으로 쏠려 감시 비중을 부풀린다 → 분석 모수에서 제외.

빅카인즈의 '오피니언' 대분류는 표집 단계(대분류=정치·경제·사회)에서 이미 빠졌고,
여기서는 경제면 사설처럼 정치·경제·사회로 태깅돼 새어든 잔여분을 제목 마커로 잡는다.
정밀도 우선(보도기사를 잘못 버리지 않게): 대괄호/꺾쇠 안에 든 칼럼명만 인정.
따라서 마커 없는 일부 의견 기사는 놓칠 수 있음(재현율 손해는 감수).
"""
import re

# 대괄호·꺾쇠 등 안에 들어가면 의견 장르로 보는 칼럼·섹션명
OPINION_KEYWORDS = [
    "사설", "시론", "칼럼", "기고", "기자수첩", "기자의 눈", "데스크",
    "만물상", "여적", "횡설수설", "분수대", "메아리", "세상읽기", "시평",
    "아침을 열며", "삶과 문화", "취재파일", "현장에서", "왜냐면", "서소문",
    "광화문에서", "노트북을 열며", "편집국에서", "기자칼럼", "시선",
]

# 소괄호 () 는 (종합)·(속보) 등 일반 제목에 흔해 제외. []·【】·<> 만 인정.
_BRACKET = re.compile(r"[\[【<]([^\]】>]{1,40})[\]】>]")
_KW = re.compile("|".join(map(re.escape, OPINION_KEYWORDS)))


def is_opinion(title) -> bool:
    """제목이 사설·칼럼 등 의견 장르면 True."""
    if not isinstance(title, str):
        return False
    for seg in _BRACKET.findall(title):
        if _KW.search(seg):
            return True
    return False
