# -*- coding: utf-8 -*-
"""
공용 LLM 프롬프트 — 단일 진실 원천(SSOT).
02_classify.py(본조사 3.4절 전체 기사 분석)와 05_llm_eval.py(골든셋 정확도 평가),
그리고 서비스 '감시 보도 코파일럿'의 평가 엔진이 모두 이 프롬프트를 공유한다.
기획안 v2 §6.4: "연구의 분석 도구와 서비스의 평가 엔진이 같은 기준·같은 프롬프트를 공유한다."

── 스키마 (기획안 v2 §6.2 화면 4영역 = 단일 LLM 호출 출력, §6.4) ──
  ① 3역할 점수(役割): 감시 / 정보·의제 / 해결책 — 분석 ③의 세 기능군과 동일 축 (각 0~100)
  ② 감시 체크리스트 5항목(표 6): 각 0~20점, 합 = 감시 점수(0~100)
       1) 공공기관_주장_검증 — 기관의 발표를 독립적 자료로 확인하였는가
       2) 근거자료_확인      — 원문·통계·문서를 직접 확인하였는가
       3) 반론_포함          — 이해관계자의 반대편 목소리가 포함되었는가
       4) 추가취재_수행      — 보도자료 외의 독자적 취재가 있는가
       5) 책임_소재_제시     — 책임의 주체를 특정하였는가
  ③ 근거(reason): 점수의 이유 한 문장(가능하면 감점 근거가 된 원문 표현 지목)
  ④ 개선제안(improvements): 무엇을 더 취재해야 하는지 행동 가능한 3가지

⚠️ 기획안 v2 §6.2 "중요": 표 6 체크리스트 5항목은 결과③(3.4절) 라벨링 기준과 반드시 동일해야 한다.
   → 라벨링 가이드·본문 표 6·이 프롬프트가 어긋나면 안 됨. 하나 고치면 셋 다 맞출 것.

── 감시 보도 조작적 정의 (2026-07-19 확정) ──────────────────────────────
  **감시 점수(체크리스트 5항목 합) ≥ 70 이면 감시 보도로 판정한다.**
  골든셋 142건 임계값 스윕 결과 t=70에서 F1 최대(0.500)이자 정밀도 1.000(오탐 0건):
      전체 정확도 93.0% / 감시 정밀도 1.000 · 재현율 0.333
  (argmax 방식은 정확도 90.1% · 정밀도 0.500 → t=70이 모든 면에서 우수)

⚠️ 재현율 0.333의 함의 — 보고서에 반드시 명시할 것:
   정밀도가 1.000이라 "감시라고 분류한 건 전부 진짜 감시"지만, 실제 감시의 약 2/3를 놓친다.
   따라서 산출되는 감시 보도 비중(%)은 **참값의 하한(과소추정)** 이다.
   보고서 논지가 "감시 보도가 적다"이므로, 이 편향이 결론에 유리한 방향임을 자각하고
   "보수적 하한값"임을 반드시 함께 밝혀야 한다. 그렇지 않으면 심사에서 정면으로 반박당한다.

💡 임계값은 나중에 바꿀 수 있다: 02_classify.py는 역할점수·체크리스트 원점수를 CSV에 저장하므로,
   t를 조정할 때 API 재호출 없이 저장된 점수에서 라벨만 다시 도출하면 된다.
"""

LABELS = ["감시", "정보·의제", "해결책"]
WATCH_THRESHOLD = 70  # 감시 판정 임계값 — 조작적 정의(위 참조). 바꾸면 본문 3.4절도 같이 고칠 것.
ROLE_AXES = ["감시", "정보·의제", "해결책"]  # 3역할 점수(기능군)
CHECKLIST_ITEMS = ["공공기관_주장_검증", "근거자료_확인", "반론_포함", "추가취재_수행", "책임_소재_제시"]

PROMPT = """당신은 언론학 내용분석 코더이자 감시 보도 코치입니다. 아래 신문 기사(언론사·제목·본문 앞부분)를
읽고, 정해진 채점표에 따라 점수를 매기고 개선 방향을 제시하세요. 당신은 "이 기사가 감시 보도인가"를
스스로 판정하는 것이 아니라, 아래에 주어진 기준을 일관되게 적용하는 실행기입니다.

[핵심 판정 원칙 — 감시의 기준]
"기자가 없었어도 이 사실이 세상에 나왔을까?"
- 그렇다(당국 발표·보도자료·재판 진행 등으로 기자가 없어도 어차피 나왔을 사실) → 감시 약함
- 아니다(기자가 스스로 입수·계산·분석·발굴해 기자 없이는 세상에 안 나왔을 사실) → 감시 강함

[A. 3역할 점수 — 각 0~100, 이 기사가 주로 수행하는 역할의 비중]
- 감시: 기자가 독자적으로 입수·계산·분석한 사실로 권력·기업·기관의 잘못·책임을 따지는 정도.
  · 수사·인사·재판의 "진행상황" 전달(착수·압수수색·소환·영장·기소·선고 등)은 [단독]이 붙어도 감시 낮음
    (발표를 당국이 한 것이지 기자가 발굴한 게 아님).
  · 제3자의 비판·논평 발언을 인용 보도하는 것도 감시 낮음(발언 전달일 뿐).
    단, 시민단체·의원실이 자체 조사·분석한 자료가 기사의 중심 소재이면 감시 높음.
- 정보·의제: 사실·현황·발표를 정확히 전달하거나 이슈를 조명·소개하는 정도(스트레이트·기획·트렌드).
- 해결책: 대안·해법을 스스로 제시하거나 해외 사례로 시사점을 도출하는 정도.
  · 기관 보고서의 제언을 단순 전달만 했다면 해결책 낮음(정보·의제에 가까움).

[B. 감시 체크리스트 5항목 — 각 0~20점, 합이 감시 점수(0~100)]
1. 공공기관_주장_검증 — 기관·공인의 발표를 기자가 독립적 자료로 교차 확인했는가.
   (발표를 그대로 옮기기만 하면 0점대, 발표 내용을 다른 근거로 검증·반박했으면 높음)
2. 근거자료_확인 — 원문·통계·문서·데이터를 기자가 직접 확인하거나 재가공했는가.
   (보도자료 수치만 인용하면 낮음, 원자료·문건을 직접 들여다봤으면 높음)
3. 반론_포함 — 이해관계자의 반대편·다른 입장 목소리가 실렸는가.
   (한쪽 주장만 실렸으면 0점대, 반론·재반론이 균형 있으면 높음)
4. 추가취재_수행 — 보도자료·발표 외에 기자의 독자적 취재(현장·인터뷰·추가 확인)가 있는가.
   (받아쓰기면 낮음, 발표에 없던 사실을 기자가 더했으면 높음)
5. 책임_소재_제시 — 문제의 책임 주체를 구체적으로 특정했는가.
   (주체가 두루뭉술하면 낮음, 누구의 무슨 잘못인지 명시하면 높음)

[C. 근거(reason)] 점수를 그렇게 준 이유를 한 문장으로. 가능하면 감점의 근거가 된 기사 속 표현을 지목.

[D. 개선제안(improvements)] 이 기사의 감시 기능을 높이려면 무엇을 더 취재해야 하는지,
   행동 가능한 형태로 정확히 3가지. (예: "결산서 원문 확인", "피해 주민 인터뷰", "담당 부처에 반론 요청")

[규칙]
- 각 점수는 정수. 3역할 점수는 서로 합이 100일 필요 없음. 체크리스트 5항목은 각 0~20.
- 감시를 높게 주려면 "기자가 없었어도 나왔을 사실인가?"에 "아니다"로 답할 수 있어야 함.
- 반드시 아래 JSON만 출력. 다른 텍스트·마크다운 금지.

{"역할점수": {"감시": 0, "정보·의제": 0, "해결책": 0}, "체크리스트": {"공공기관_주장_검증": 0, "근거자료_확인": 0, "반론_포함": 0, "추가취재_수행": 0, "책임_소재_제시": 0}, "reason": "판단 근거 한 문장", "improvements": ["제안1", "제안2", "제안3"]}

[기사]
언론사: {press}
제목: {title}
본문(앞부분): {body}
"""


def build_prompt(press, title, body):
    return PROMPT.replace("{press}", str(press)) \
                 .replace("{title}", str(title)) \
                 .replace("{body}", str(body)[:220])


def parse_response(j: dict) -> dict:
    """LLM JSON 응답 → 표준 dict. 결측/오타 방어."""
    roles = j.get("역할점수", {}) or {}
    checks = j.get("체크리스트", {}) or {}
    role_scores = {ax: _int0(roles.get(ax)) for ax in ROLE_AXES}
    check_scores = {it: _int0(checks.get(it)) for it in CHECKLIST_ITEMS}
    watch_score = sum(check_scores.values())  # 감시 점수(0~100) = 체크리스트 5항목 합
    improvements = j.get("improvements", []) or []
    if not isinstance(improvements, list):
        improvements = [str(improvements)]
    return {
        "역할_감시": role_scores["감시"],
        "역할_정보·의제": role_scores["정보·의제"],
        "역할_해결책": role_scores["해결책"],
        "감시점수": watch_score,
        **{f"체크_{it}": check_scores[it] for it in CHECKLIST_ITEMS},
        "reason": j.get("reason", ""),
        "improvements": improvements[:3],
    }


def derive_label(parsed: dict, threshold: int = WATCH_THRESHOLD) -> str:
    """3분류 라벨 — 확정된 조작적 정의(2026-07-19).
    감시 점수(체크리스트 5항목 합) ≥ threshold 이면 감시, 아니면 정보·의제 vs 해결책 중 높은 쪽."""
    if parsed["감시점수"] >= threshold:
        return "감시"
    return "정보·의제" if parsed["역할_정보·의제"] >= parsed["역할_해결책"] else "해결책"


def derive_label_argmax(parsed: dict) -> str:
    """(대안·비교용) 3역할 점수 argmax 방식. 골든셋 정확도 90.1%로 t=70(93.0%)보다 낮아 채택되지 않음."""
    w, ia, s = parsed["역할_감시"], parsed["역할_정보·의제"], parsed["역할_해결책"]
    best = max(w, ia, s)
    if w == best:
        return "감시"
    if s == best:
        return "해결책"
    return "정보·의제"


def derive_confidence(parsed: dict, threshold: int = WATCH_THRESHOLD) -> str:
    """확신도 = 판정을 가른 값이 경계에서 얼마나 떨어져 있나(경계 근처일수록 불확실).
    감시 판정은 감시점수와 임계값의 거리로, 비감시는 정보·의제/해결책 점수차로 본다."""
    margin = abs(parsed["감시점수"] - threshold)
    if parsed["감시점수"] < threshold:
        # 감시가 아닌 경우, 정보·의제 vs 해결책 구분의 확실성도 함께 고려
        margin = min(margin, abs(parsed["역할_정보·의제"] - parsed["역할_해결책"]))
    if margin >= 30:
        return "높음"
    if margin >= 15:
        return "중간"
    return "낮음"


def _int0(v):
    try:
        return int(round(float(v)))
    except (TypeError, ValueError):
        return 0
